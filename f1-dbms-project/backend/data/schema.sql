-- Create DB
CREATE DATABASE IF NOT EXISTS f1DBMS;
USE f1DBMS;

DROP TABLE IF EXISTS CHAMPIONSHIPS;
DROP TABLE IF EXISTS RESULTS;
DROP TABLE IF EXISTS RACES;
DROP TABLE IF EXISTS PITWALL;
DROP TABLE IF EXISTS CARS;
DROP TABLE IF EXISTS CIRCUITS;
DROP TABLE IF EXISTS DRIVER_NUMBERS;
DROP TABLE IF EXISTS DRIVERS;
DROP TABLE IF EXISTS SEASON_TEAMS;
DROP TABLE IF EXISTS TEAMS;
DROP TABLE IF EXISTS USERS;
DROP TABLE IF EXISTS ANNOUNCEMENTS;
DROP TABLE IF EXISTS VISIT_DEPARTMENTS;
DROP TABLE IF EXISTS VISITS;

DROP VIEW IF EXISTS ADMIN_VISIT_OVERVIEW;
DROP VIEW IF EXISTS FULL_VISIT_DETAILS;

CREATE TABLE IF NOT EXISTS TEAMS (
    TeamID VARCHAR(3) PRIMARY KEY, -- ALP, AMR, WIL, AUD, CAD, FER, HAS, MCL, MER, RB and ORB
    TeamName VARCHAR(30) NOT NULL
);

CREATE TABLE IF NOT EXISTS SEASON_TEAMS (
	TeamID VARCHAR(3),
    Season YEAR NOT NULL,
    DisplayName VARCHAR(50),
    Color CHAR(7),
	PRIMARY KEY (TeamID, Season),
    FOREIGN KEY (TeamID) REFERENCES TEAMS(TeamID)
);

CREATE TABLE IF NOT EXISTS DRIVERS (
    DriverID INT PRIMARY KEY AUTO_INCREMENT, -- HAM85        Abrrev  +  birth year
    Abbrev VARCHAR(3),
    FirstName VARCHAR(50),
    LastName VARCHAR(50),
    Nationality VARCHAR(50),
    DOB DATE,
    DebutYear INT,
    DriverRole ENUM('Main', 'Reserve', 'Test', 'Retired', 'Test/Reserve') -- Main, Reserve, Test
);

CREATE TABLE IF NOT EXISTS DRIVER_NUMBERS (
    DriverID INT PRIMARY KEY,
    Season YEAR,
    DriverNumber INT,
    DriverImage VARCHAR(255),
    FOREIGN KEY (DriverID) REFERENCES DRIVERS(DriverID)
);

CREATE TABLE IF NOT EXISTS CARS (
    CarID INT PRIMARY KEY AUTO_INCREMENT, 
    TeamID VARCHAR(3),
    Chassis VARCHAR(10), -- RB21
    Season YEAR,
    PowerUnit VARCHAR(20),
    EngineSupplier VARCHAR(30),  -- Mercedes
    CarImage VARCHAR(255),
    FOREIGN KEY (TeamID) REFERENCES TEAMS(TeamID)
);

CREATE TABLE IF NOT EXISTS PITWALL (
    StaffID INT PRIMARY KEY AUTO_INCREMENT,
    TeamID VARCHAR(3),
    DriverID INT NULL,
    StaffName VARCHAR(100),
    StaffRole VARCHAR(100),
    Season YEAR,
    FOREIGN KEY (TeamID) REFERENCES TEAMS(TeamID),
    FOREIGN KEY (DriverID) REFERENCES DRIVERS(DriverID)
);

CREATE TABLE IF NOT EXISTS CIRCUITS (
    CircuitID INT PRIMARY KEY AUTO_INCREMENT,
    CircuitName VARCHAR(30),
    Country VARCHAR(30),
    CircuitLength_km DECIMAL(5,2),
    Turns INT
);

CREATE TABLE IF NOT EXISTS RACES (
    RaceID INT PRIMARY KEY AUTO_INCREMENT,
    CircuitID INT,
    Season YEAR,
    RaceNumber INT,
    RaceName VARCHAR(100), -- Bahrain, Spa...
    RaceDate DATE,
    Laps INT,
    Weather VARCHAR(50),
    FOREIGN KEY (CircuitID) REFERENCES CIRCUITS(CircuitID)
);

CREATE TABLE IF NOT EXISTS RESULTS (
    ResultID INT PRIMARY KEY AUTO_INCREMENT,
    RaceID INT,
    DriverID INT,
    TeamID VARCHAR(3),
    Pos INT NULL,
    Points INT DEFAULT 0,
    Standings ENUM('Finished','DNF','DSQ', 'DNS', 'DNQ', 'NC', 'Retired'), -- Finished, DNF (Did Not Finish), DSQ (Disqualified), DNS (Did Not Start), DNQ (Did Not Qualify), NC (Not Classified), Retired
    LapTime VARCHAR(20) NULL,
    FOREIGN KEY (RaceID) REFERENCES RACES(RaceID),
    FOREIGN KEY (DriverID) REFERENCES DRIVERS(DriverID),
    FOREIGN KEY (TeamID) REFERENCES TEAMS(TeamID)
);

CREATE TABLE IF NOT EXISTS CHAMPIONSHIPS (
    ChampionID VARCHAR(7) PRIMARY KEY, -- MAX2021 or MCL2025
    Season YEAR,
    Category ENUM('Drivers','Constructors'),
    DriverID INT NULL,
    TeamID VARCHAR(3) NULL,
    Points INT,
    Wins INT,
    FOREIGN KEY (DriverID) REFERENCES DRIVERS(DriverID),
    FOREIGN KEY (TeamID) REFERENCES TEAMS(TeamID)
);

CREATE TABLE IF NOT EXISTS VISITS (
    FormID INT PRIMARY KEY AUTO_INCREMENT,

    FullName VARCHAR(100),
    Email VARCHAR(100) CHECK (Email LIKE '%@%.%'),
    Phone CHAR(10) UNIQUE,
    Gender VARCHAR(20),
    DOB DATE,

    Team VARCHAR(50),
    PreferredDate DATE,
    TotalVisitors INT,
    TourDuration ENUM('Half-Day','Full-Day'),
    SpecialRequests VARCHAR(100),

    SubmitTime TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    LastUpdate TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    VisitStatus ENUM('Pending','Approved','Completed','Cancelled') DEFAULT 'Pending' NULL
);	

CREATE TABLE IF NOT EXISTS VISIT_DEPARTMENTS (
    FormID INT,
    Department ENUM('Research','Development','Testing'),
    PRIMARY KEY (FormID, Department),
    FOREIGN KEY (FormID) REFERENCES VISITS(FormID)
);

CREATE TABLE IF NOT EXISTS USERS (
    UserID INT PRIMARY KEY AUTO_INCREMENT,
    FormID INT NULL,
    Username VARCHAR(50) UNIQUE,
    Email VARCHAR(100) UNIQUE,
    UserPassword VARCHAR(255),
    Role ENUM(
        'admin',
        'user'
    ) DEFAULT 'user',
    CreatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (FormID) REFERENCES VISITS(FormID)
);

CREATE TABLE IF NOT EXISTS ANNOUNCEMENTS (

    AnnouncementID INT PRIMARY KEY AUTO_INCREMENT,

    Title VARCHAR(120),

    Content TEXT,

    CreatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE VIEW ADMIN_VISIT_OVERVIEW AS
SELECT
    FullName,
    Email,
    Team,
    PreferredDate,
    TotalVisitors
FROM VISITS;

CREATE VIEW FULL_VISIT_DETAILS AS
SELECT *
FROM VISITS;

CREATE INDEX idx_results_driver
ON RESULTS(DriverID);

CREATE INDEX idx_results_race
ON RESULTS(RaceID);

CREATE INDEX idx_races_season
ON RACES(Season);

CREATE INDEX idx_visit_status
ON VISITS(VisitStatus);

CREATE INDEX idx_users_username
ON USERS(Username);

CREATE INDEX idx_championship_driver
ON CHAMPIONSHIPS(DriverID);